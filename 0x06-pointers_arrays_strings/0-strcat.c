/**
 * *_strcat - Concatenates two strings
 * @dest: the destination string
 * @src: the source strings
 *Return: string
 *
 */

char *_strcat(char *dest, char *src)
{
	int len1, len2;
	int i;

	for (i < 0; i <= len2; i++)
	{
		dest[len1 + 1] = src[i];
			i++;

	}
	dest[i] = '\0';
	return (dest);

}
